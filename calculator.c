#include <stdio.h>

int main() {
    int num = 10;

    // Check if num is greater than 0
    if (num > 0) {
        printf("The number is positive\n");
    }

    // Check if num is even or odd
    if (num % 2 == 0) {
        printf("The number is even\n");
    } else {
        printf("The number is odd\n");
    }

    return 0;
}
